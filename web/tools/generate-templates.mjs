// Generate web/src/handImage/glyphTemplates.ts from the sample images.
// Clusters glyphs (deterministic, same order as glyph-cluster.mjs) and writes
// each cluster centroid with the rank label assigned from the montage.
import { PNG } from "pngjs";
import { readFileSync, writeFileSync, readdirSync, mkdtempSync } from "fs";
import { execSync } from "child_process";
import { tmpdir } from "os";
import { join } from "path";

// montage order (#0..#13) read off /tmp/.../glyph-montage.png
const LABELS = ["A", "T", "K", "Q", "4", "2", "6", "5", "7", "3", "8", "J", "9", "9"];

const DIR = "/Users/takenokogohan/sandbox/poker-analyzer/example_images";
const out = join(mkdtempSync(join(tmpdir(), "tf-")), "detect.mjs");
execSync(
  `npx esbuild ${new URL("../src/handImage/detect.ts", import.meta.url).pathname} --bundle --format=esm --outfile=${out}`,
  { stdio: "pipe" }
);
const { detectCards, extractGlyph, glyphDist, GLYPH_W, GLYPH_H } = await import(out);

const glyphs = [];
for (const f of readdirSync(DIR).filter((f) => /\.png$/i.test(f)).sort()) {
  const png = PNG.sync.read(readFileSync(join(DIR, f)));
  const img = { width: png.width, height: png.height, data: png.data };
  const d = detectCards(img);
  const slots = [];
  for (const row of d.holeRows) for (const c of row.cards) slots.push([c.cx, row.cy]);
  for (const c of d.board) slots.push([c.cx, c.cy]);
  for (const [cx, cy] of slots) {
    const g = extractGlyph(img, cx, cy, d.chipW, d.chipH);
    if (g) glyphs.push(g);
  }
}
const TH = 0.12;
const clusters = [];
for (const g of glyphs) {
  let best = -1, bd = 1;
  for (let i = 0; i < clusters.length; i++) {
    const dd = glyphDist(g, clusters[i].centroid);
    if (dd < bd) { bd = dd; best = i; }
  }
  if (best >= 0 && bd < TH) clusters[best].members.push(g);
  else clusters.push({ centroid: g, members: [g] });
}
for (const c of clusters) {
  const cen = new Uint8Array(GLYPH_W * GLYPH_H);
  for (let k = 0; k < cen.length; k++) {
    let s = 0; for (const m of c.members) s += m[k];
    cen[k] = s * 2 >= c.members.length ? 1 : 0;
  }
  c.centroid = cen;
}
clusters.sort((a, b) => b.members.length - a.members.length);

if (clusters.length !== LABELS.length)
  throw new Error(`cluster count ${clusters.length} != labels ${LABELS.length}`);

const entries = clusters.map((c, i) => {
  const bits = Array.from(c.centroid).join("");
  return `  { label: "${LABELS[i]}", bits: "${bits}" },`;
});
const ts = `// AUTO-GENERATED by tools/generate-templates.mjs — do not edit by hand.
// Rank glyph templates for the Ten-Four hand-history font (${GLYPH_W}x${GLYPH_H}).
export const GLYPH_TEMPLATES: { label: string; bits: string }[] = [
${entries.join("\n")}
];
`;
const dest = new URL("../src/handImage/glyphTemplates.ts", import.meta.url).pathname;
writeFileSync(dest, ts);
console.log("wrote", dest, "with", clusters.length, "templates");
